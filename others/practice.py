def add(x,y):
    return x + y
def sub(x,y):
    return x - y
def mul(x,y):
    return x * y
def div(x,y):
    return x / y
def mod(x,y):
    return x % y
def pow(x,y):
    return x ** y
def floordiv(x,y):
    return x // y

